"""
Multi-Agent LangGraph Workflow with Application Insights and Custom Tools

This script demonstrates LangChain/LangGraph multi-agent workflow using
the native langchain-azure-ai integration for Application Insights telemetry.

Architecture:
  User Query
      │
      ▼
  ┌─────────────┐  Tools:          ┌─────────────────┐  Tools:
  │  Researcher │  - search_web    │  Summarizer     │  - format_article
  │   Agent     │  - statistics    │   Agent         │  - add_citations
  │             │  - verify_facts  │                 │
  └─────────────┘                  └─────────────────┘
      │                                     │
      └───────────── Traces ────────────────┘
                        │
                        ▼
              Application Insights

Observability Approach:
- AzureAIOpenTelemetryTracer: Automatic tracing via LangChain callbacks
- trace_all_langgraph_nodes=True: CRITICAL parameter for proper LangGraph tracing
- NO custom OpenTelemetry spans - the tracer handles everything through callbacks
- Callback-based instrumentation automatically creates spans for:
  * LangGraph workflow execution
  * Each agent node invocation (with proper timing when trace_all_langgraph_nodes=True)
  * Each LLM call (with GenAI semantic conventions)
  * Each tool execution
  
Expected trace hierarchy (auto-generated by tracer with trace_all_langgraph_nodes=True):
    LangGraph workflow
    ├── invoke_agent researcher
    │   ├── chat (LLM call with gen_ai.* attributes)
    │   ├── execute_tool search_web
    │   ├── execute_tool gather_statistics
    │   └── chat (final response)
    └── invoke_agent summarizer
        ├── chat (LLM call)
        ├── execute_tool format_article
        └── chat (final response)

Key Pattern:
- Uses LangGraph's ToolNode for automatic tool execution
- Proper ToolMessage responses with tool_call_id
- Conditional routing between agents and tools
- All traces captured automatically by AzureAIOpenTelemetryTracer
- Connection string prevents managed identity token calls (avoids NewConnectionError)
"""

