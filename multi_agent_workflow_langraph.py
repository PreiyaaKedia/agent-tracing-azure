def main():
    """Main execution function"""
    
    print("\n" + "="*80)
    print("LANGCHAIN/LANGGRAPH MULTI-AGENT WORKFLOW WITH TOOLS")
    print("="*80)
    print("Using langchain-azure-ai native Application Insights integration")
    print("Key setting: trace_all_langgraph_nodes=True for proper node tracing")
    print("Tools: search_web, gather_statistics, verify_facts, format_article, add_citations")
    print("="*80 + "\n")
    
    # Create the Azure AI OpenTelemetry Tracer
    tracer = create_tracer()
    
    # Initialize Azure OpenAI LLM with token provider for authentication
    token_provider = get_bearer_token_provider(
        DefaultAzureCredential(),
        "https://cognitiveservices.azure.com/.default"
    )
    
    llm = AzureChatOpenAI(
        azure_endpoint=os.getenv("AZURE_OPENAI_ENDPOINT"),
        azure_deployment=os.getenv("AZURE_OPENAI_CHAT_DEPLOYMENT"),
        api_version=os.getenv("AZURE_OPENAI_VERSION", "2024-08-01-preview"),
        azure_ad_token_provider=token_provider,
        temperature=0.3,
    )
    
    # Build the compiled LangGraph app
    app = build_workflow(llm)
    
    # Run sample queries that will definitely trigger tool use
    queries = [
        "Search for the latest breakthroughs in quantum computing and provide statistics on market growth.",
        # More explicit query to ensure tool calling
    ]
    
    for q in queries:
        try:
            run_workflow(q, app, tracer)
            time.sleep(1)  # Brief pause between workflows
        except Exception as e:
            print(f"[!] Workflow failed for query '{q}': {e}")
            continue
    
    print("\n" + "="*80)
    print("ALL WORKFLOWS COMPLETED")
    print("Check Application Insights for detailed telemetry data")
    print("="*80 + "\n")


if __name__ == "__main__":
    main()
