# ─────────────────────────────────────────────────────────────────────────────
# 1.  Create the Azure AI OpenTelemetry Tracer
# ─────────────────────────────────────────────────────────────────────────────

def create_tracer() -> AzureAIOpenTelemetryTracer:
    """
    Create an AzureAIOpenTelemetryTracer that automatically sends traces
    to Application Insights.
    
    The tracer can be configured with:
    - connection_string: Direct Application Insights connection string
    - project_endpoint: Azure AI Project endpoint (auto-discovers App Insights)
    - enable_content_recording: Whether to include message content in traces
    - trace_all_langgraph_nodes: Enable proper LangGraph node tracing (IMPORTANT!)
    """
    
    # Prefer connection string to avoid managed identity token calls
    connection_string = os.getenv("APPLICATION_INSIGHTS_CONNECTION_STRING")
    
    if connection_string:
        print(f"[✓] Using direct Application Insights connection string")
        tracer = AzureAIOpenTelemetryTracer(
            connection_string=connection_string,
            enable_content_recording=True,  # Set False in production to redact content
            trace_all_langgraph_nodes=True,  # CRITICAL for LangGraph - enables proper node tracing
        )
    else:
        # Fallback to project endpoint (requires managed identity)
        project_endpoint = os.getenv("AZURE_AI_PROJECT_ENDPOINT")
        if project_endpoint:
            print(f"[✓] Using Azure AI Project endpoint: {project_endpoint}")
            tracer = AzureAIOpenTelemetryTracer(
                project_endpoint=project_endpoint,
                credential=DefaultAzureCredential(),
                enable_content_recording=True,
                trace_all_langgraph_nodes=True,  # CRITICAL for LangGraph
            )
        else:
            raise ValueError(
                "Either APPLICATION_INSIGHTS_CONNECTION_STRING or AZURE_AI_PROJECT_ENDPOINT "
                "must be set in environment variables"
            )
    
    print("[✓] LangChain Azure AI tracer configured")
    return tracer


