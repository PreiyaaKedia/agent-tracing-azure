# Research tools for the Researcher agent
research_tools = [search_web, gather_statistics, verify_facts]

# Writing tools for the Summarizer agent
writing_tools = [format_article, add_citations]


# ─────────────────────────────────────────────────────────────────────────────
# 4.  Agent 1 – Researcher with Tools
# ─────────────────────────────────────────────────────────────────────────────

def make_researcher_agent(llm: AzureChatOpenAI):
    """
    Create researcher agent that uses research tools.
    Returns the bound model and a node function.
    """
    # Bind research tools to the LLM
    researcher_llm = llm.bind_tools(research_tools)
    
    def researcher_node(state: MessagesState):
        """
        Researcher agent node - automatically traced by AzureAIOpenTelemetryTracer.
        Tool calls are handled by ToolNode.
        No custom spans here - let LangChain's tracer handle everything.
        """
        print(f"\n{'─'*80}")
        print(f"[Researcher] Processing query...")
        
        messages = state["messages"]
        
        # Check if we've already used tools
        has_used_tools = any(hasattr(msg, '__class__') and msg.__class__.__name__ == 'ToolMessage' for msg in messages)
        
        # Add system message if this is the first message
        if not any(isinstance(msg, SystemMessage) for msg in messages):
            system_msg = SystemMessage(content=(
                "You are a research assistant with access to powerful research tools. "
                "IMPORTANT: You MUST use at least one tool to gather information. "
                "Available tools:\n"
                "- search_web: Search for current information and facts\n"
                "- gather_statistics: Get statistical data and numbers\n"
                "- verify_facts: Verify accuracy of claims\n\n"
                "ALWAYS start by calling search_web with the user's topic. "
                "After receiving tool results, provide a comprehensive research summary with 3-5 key findings."
            ))
            messages = [system_msg] + messages
        
        # If we've already used tools, force a final response without tools
        if has_used_tools:
            print(f"[Researcher] Tools already used, generating final research summary...")
            # Use LLM without tools to force final response
            final_instruction = HumanMessage(content=(
                "Now provide your comprehensive research summary based on all the tool results above. "
                "Do not call any more tools, just provide the summary with 3-5 key findings."
            ))
            response = llm.invoke(messages + [final_instruction])
        else:
            # First pass - allow tool use
            response = researcher_llm.invoke(messages)
        
        if hasattr(response, 'tool_calls') and response.tool_calls:
            print(f"[Researcher] Making {len(response.tool_calls)} tool call(s)...")
        else:
            print(f"[Researcher] Completed research.")
        
        return {"messages": [response]}
    
    return researcher_llm, researcher_node


# ─────────────────────────────────────────────────────────────────────────────
# 5.  Agent 2 – Summarizer with Tools
# ─────────────────────────────────────────────────────────────────────────────

def make_summarizer_agent(llm: AzureChatOpenAI):
    """
    Create summarizer agent that uses writing/formatting tools.
    Returns the bound model and a node function.
    """
    # Bind writing tools to the LLM
    summarizer_llm = llm.bind_tools(writing_tools)
    
    def summarizer_node(state: MessagesState):
        """
        Summarizer agent node - automatically traced by AzureAIOpenTelemetryTracer.
        Tool calls are handled by ToolNode.
        No custom spans here - let LangChain's tracer handle everything.
        """
        print(f"\n{'─'*80}")
        print(f"[Summarizer] Creating summary...")
        
        messages = state["messages"]
        
        # Check if we've already used tools in the summarizer phase
        has_used_tools = False
        summarizer_started = False
        for msg in messages:
            if hasattr(msg, 'content') and isinstance(msg.content, str) and 'Based on the research above' in msg.content:
                summarizer_started = True
            if summarizer_started and hasattr(msg, '__class__') and msg.__class__.__name__ == 'ToolMessage':
                has_used_tools = True
                break
        
        # If we haven't added the summary instruction yet, add it
        if not any(hasattr(msg, 'content') and isinstance(msg.content, str) and 'Based on the research above' in msg.content for msg in messages):
            summary_instruction = HumanMessage(content=(
                "Based on the research above, create a clear, engaging 2-3 sentence "
                "summary suitable for a general audience."
            ))
            messages = messages + [summary_instruction]
        
        # If we've already used tools, force a final response without tools
        if has_used_tools:
            print(f"[Summarizer] Tools already used, generating final summary...")
            # Use LLM without tools to force final response
            final_instruction = HumanMessage(content=(
                "Now provide your final summary based on all the information above. "
                "Do not call any more tools, just give the summary."
            ))
            response = llm.invoke(messages + [final_instruction])
        else:
            # First pass - allow tool use
            response = summarizer_llm.invoke(messages)
        
        if hasattr(response, 'tool_calls') and response.tool_calls:
            print(f"[Summarizer] Making {len(response.tool_calls)} tool call(s)...")
        else:
            print(f"[Summarizer] Completed summary.")
        
        return {"messages": [response]}
    
    return summarizer_llm, summarizer_node


# ─────────────────────────────────────────────────────────────────────────────
# 6.  Routing Logic
# ─────────────────────────────────────────────────────────────────────────────

def route_researcher(state: MessagesState):
    """Route from researcher: continue to tools or move to summarizer."""
    messages = state["messages"]
    last_message = messages[-1]
    # If the last message has tool calls, route to tools
    if hasattr(last_message, "tool_calls") and last_message.tool_calls:
        return "researcher_tools"
    # Otherwise, move to summarizer
    return "summarizer"


def route_summarizer(state: MessagesState):
    """Route from summarizer: continue to tools or end."""
    messages = state["messages"]
    last_message = messages[-1]
    # If the last message has tool calls, route to tools
    if hasattr(last_message, "tool_calls") and last_message.tool_calls:
        return "summarizer_tools"
    # Otherwise, we're done
    return "end"


# ─────────────────────────────────────────────────────────────────────────────
# 7.  Build and compile the LangGraph workflow
# ─────────────────────────────────────────────────────────────────────────────

def build_workflow(llm: AzureChatOpenAI):
    """
    Assembles the multi-agent graph with automatic tool execution:
    
    START → researcher → [tools?] → summarizer → [tools?] → END
    
    Uses ToolNode to automatically handle tool calls with proper ToolMessage
    responses including tool_call_id.
    """
    # Create agents
    researcher_llm, researcher_node = make_researcher_agent(llm)
    summarizer_llm, summarizer_node = make_summarizer_agent(llm)
    
    # Create ToolNodes for automatic tool execution
    researcher_tool_node = ToolNode(research_tools)
    summarizer_tool_node = ToolNode(writing_tools)
    
    # Build the graph
    workflow = StateGraph(MessagesState)
    
    # Add agent nodes
    workflow.add_node("researcher", researcher_node)
    workflow.add_node("summarizer", summarizer_node)
    
    # Add tool nodes
    workflow.add_node("researcher_tools", researcher_tool_node)
    workflow.add_node("summarizer_tools", summarizer_tool_node)
    
    # Set entry point
    workflow.add_edge(START, "researcher")
    
    # Add conditional edges from researcher
    workflow.add_conditional_edges(
        "researcher",
        route_researcher,
        {
            "researcher_tools": "researcher_tools",
            "summarizer": "summarizer",
        }
    )
    
    # After researcher tools, go back to researcher
    workflow.add_edge("researcher_tools", "researcher")
    
    # Add conditional edges from summarizer
    workflow.add_conditional_edges(
        "summarizer",
        route_summarizer,
        {
            "summarizer_tools": "summarizer_tools",
            "end": END,
        }
    )
    
    # After summarizer tools, go back to summarizer
    workflow.add_edge("summarizer_tools", "summarizer")
    
    return workflow.compile()


# ─────────────────────────────────────────────────────────────────────────────
# 8.  Run the workflow with the tracer
# ─────────────────────────────────────────────────────────────────────────────

def run_workflow(query: str, app, tracer: AzureAIOpenTelemetryTracer) -> dict:
    """
    Run the workflow with the AzureAIOpenTelemetryTracer.
    The tracer automatically creates spans for all operations via LangChain callbacks.
    
    NO custom parent span - let the tracer create the proper hierarchy automatically.
    """
    print(f"\n{'═'*80}")
    print(f" LangGraph Multi-Agent Workflow with Tools")
    print(f"{'═'*80}")
    print(f" Query: {query}")

    try:
        # Initial state with the user query
        initial_state = {"messages": [HumanMessage(content=query)]}

        # Pass the tracer via config callbacks - this is the key!
        # The tracer will automatically create spans for:
        # - LangGraph workflow execution
        # - Each agent node invocation
        # - Each LLM call (with GenAI semantic conventions)
        # - Each tool execution
        config = {"callbacks": [tracer]}
        
        start = time.monotonic()
        result = app.invoke(initial_state, config=config)
        elapsed = time.monotonic() - start

        # Extract final message
        final_message = result["messages"][-1]
        final_content = final_message.content if hasattr(final_message, 'content') else str(final_message)
        
        # Count tool uses
        tool_messages = [msg for msg in result["messages"] if hasattr(msg, '__class__') and msg.__class__.__name__ == 'ToolMessage']
        tools_used = len(tool_messages)
        
        print(f"\n{'═'*80}")
        print(" FINAL RESULT")
        print(f"{'═'*80}")
        print(final_content)
        print(f"\n Tools Executed: {tools_used}")
        print(f" Completed in {elapsed:.2f}s")
        print(f"{'═'*80}\n")

        return result
        
    except Exception as e:
        print(f"[!] Workflow error: {e}")
        raise


